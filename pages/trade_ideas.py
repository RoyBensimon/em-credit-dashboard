# ── Z-score colour helper ─────────────────────────────────────────────────────

def _zscore_color(z: float) -> str:
    if abs(z) >= 2.5:
        return COLORS["negative"]
    if abs(z) >= 2.0:
        return COLORS["warning"]
    return COLORS["primary"]


def _zscore_badge(z: float) -> str:
    color = _zscore_color(z)
    sign  = "+" if z > 0 else ""
    label = "STRONG" if abs(z) >= 2.5 else "SIGNAL"
    return (
        f"<span style='background:{color};color:#fff;padding:2px 8px;"
        f"border-radius:4px;font-size:11px;font-weight:700'>"
        f"{label} Z={sign}{z:.1f}σ</span>"
    )


